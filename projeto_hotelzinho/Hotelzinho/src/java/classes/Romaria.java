public class Romaria {
    
    /*
        Atributos da Classe
    */
    private int codigo;
    private int qdtCasais;
    private int qdtSolteiros;
    private int qdtCriancas;

    /*
        Getter e Setter da Classe
    */

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    
    public int getQdtCasais() {
        return qdtCasais;
    }

    public void setQdtCasais(int qdtCasais) {
        this.qdtCasais = qdtCasais;
    }

    public int getQdtSolteiros() {
        return qdtSolteiros;
    }

    public void setQdtSolteiros(int qdtSolteiros) {
        this.qdtSolteiros = qdtSolteiros;
    }

    public int getQdtCriancas() {
        return qdtCriancas;
    }

    public void setQdtCriancas(int qdtCriancas) {
        this.qdtCriancas = qdtCriancas;
    }
}
